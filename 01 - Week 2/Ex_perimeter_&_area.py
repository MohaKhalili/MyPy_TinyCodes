# A program to calculate area & perimeter of Rectangle
# this is written on 98 tir

length_Rectangle = 4 # input the length of Rectangle
width_Rectangle = 3 # input the width of Rectangle

# do the calculation
area_Rectangle = length_Rectangle * width_Rectangle
perimeter_Rectangle = (length_Rectangle + width_Rectangle)*2

# print the output
print("area of the rectangle is: ",area_Rectangle)
print("perimeter of the rectangle is: ",perimeter_Rectangle)